  <h3>1. Aceptación y Relación Contractual</h3>
  <p>
    Al acceder, navegar o utilizar este sitio web, así como al proporcionar sus datos personales a través de formularios digitales o mediante WhatsApp, el usuario acepta plenamente estos Términos, Condiciones y Aviso de Privacidad. Si el usuario no está de acuerdo con los presentes términos, deberá abstenerse de utilizar los servicios ofrecidos por LeadConnect.
  </p>

  <h3>2. Descripción del Servicio</h3>
  <p>LeadConnect ofrece servicios de intermediación digital para la contratación de planes y portabilidad hacia AT&T, los cuales incluyen:</p>
  <ul>
    <li>Recepción y validación de documentos de identidad.</li>
    <li>Gestión del NIP de Portabilidad numérica.</li>
    <li>Seguimiento del proceso de migración de línea.</li>
    <li>Coordinación de entrega de tarjetas SIM.</li>
    <li>Activación de planes y servicios de telecomunicaciones AT&T.</li>
  </ul>
  <p>
    El usuario declara ser titular de la línea telefónica a portar o contar con autorización expresa del titular conforme a la normativa del Instituto Federal de Telecomunicaciones (IFT).
  </p>

  <h3>3. Datos Personales Recopilados</h3>
  <p>
    Para la correcta prestación del servicio y cumplimiento de obligaciones regulatorias, LeadConnect podrá recabar los siguientes datos:
  </p>
  <ul>
    <li>Nombre completo del titular.</li>
    <li>Identificación oficial vigente (INE o Pasaporte).</li>
    <li>CURP.</li>
    <li>Número de teléfono celular.</li>
    <li>Correo electrónico.</li>
    <li>Compañía telefónica de origen.</li>
    <li>NIP de Portabilidad.</li>
  </ul>

  <h3>4. Uso de WhatsApp Business y Consentimiento (Opt-In)</h3>
  <p>
    Al proporcionar su número telefónico y enviar cualquiera de nuestros formularios, el usuario otorga su consentimiento expreso para ser contactado por LeadConnect a través de WhatsApp Business con fines relacionados exclusivamente a:
  </p>
  <ul>
    <li>Seguimiento del trámite de portabilidad.</li>
    <li>Solicitud de documentación faltante.</li>
    <li>Notificación de estatus del proceso.</li>
    <li>Soporte técnico asociado a la activación del servicio.</li>
    <li>Confirmación de entrega de SIM.</li>
  </ul>
  <p>
    La frecuencia de los mensajes dependerá de la etapa del trámite. Pueden aplicarse tarifas de datos conforme a su operador móvil.
  </p>

  <h3>5. Cancelación de Comunicaciones (Opt-Out)</h3>
  <p>
    El usuario podrá revocar en cualquier momento su consentimiento para recibir mensajes:
  </p>
  <ul>
    <li>Enviando la palabra <strong>STOP</strong> o <strong>BAJA</strong> vía WhatsApp.</li>
    <li>Solicitándolo por correo electrónico a: <strong>contacto@leadconnect.mx</strong></li>
  </ul>
  <p>
    Una vez procesada la solicitud, LeadConnect suspenderá las comunicaciones no operativas.
  </p>

